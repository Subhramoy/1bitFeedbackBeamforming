    int
    tx_packet_source_impl::work(int noutput_items,
        gr_vector_const_void_star &input_items,
        gr_vector_void_star &output_items)
    {
        const float *in1    = (const float *) input_items[0];
		const float *in2    = (const float *) input_items[1];
		const uint64_t *in3 = (const uint64_t *) input_items[2];
		const double *in4   = (const double *) input_items[3];
		gr_complex *out     = (gr_complex *) output_items[0];
      // Do <+signal processing+>
      
      for (int i = 0; i < (noutput_items); i++){ 
			cpt_samples++;
			
			// Monitoring input of block
	// -------------------------
			if (in1[i] != 0) {
				// Packet with known time-stamp received!
				fpga_time_available = true;
				lo_phase_offset = in1[i];
				lo_freq_offset = in2[i];
				secs_rx = in3[i];
				fracs_rx = in4[i];
				//std::cout <<  boost::format("[Rx packet] Full seconds %u, Frac seconds %f") % secs_rx % fracs_rx << std::endl;
				// Calculating time-stamp for packet to be transmitted!
				fracs_tx 	= fracs_rx + d_time_delay; 
				double intpart; //normalize
				fracs_tx 	= std::modf(fracs_tx, &intpart);
                secs_tx  	= secs_rx + uint64_t(intpart);
				//std::cout <<  boost::format("[Tx packet] Full seconds %u, Frac seconds %f") % secs_tx % fracs_tx << std::endl;
				} // End if(in3[i] != 0)
				
		// Generating output of block
		// --------------------------
		out[i] = gr_complex(0,0);		
		if (cpt_samples == 1){
	    // Make SOB-tag and time-tag for first burst of zeros
	    this->make_sob_tag(this->nitems_written(0)+i);
            //this->make_time_tag(this->nitems_written(0)+i); 
		}	
	
		if (fpga_time_available) {	
	    cpt_samples_packet ++;
	    // --- Compensating for LO clock offset --- //
	    output_phase_ref_freq = (lo_phase_offset +(cpt_samples_packet-1)*(lo_freq_offset*Ts));
	    output_phase_bf_freq  = fmod( (d_f_out/d_f_in)*output_phase_ref_freq ,2*pi);
	    // ---------------------------------------- //
	    out[i] = tx_packet[cpt_samples_packet-1] * gr_complex(std::cos(output_phase_bf_freq),std::sin(output_phase_bf_freq));
	
	    if (cpt_samples_packet == 1){
		// Put EOB-tag for previous burst of zeros
		this->make_eob_tag(this->nitems_written(0) + i-1); 

		std::cout <<  boost::format("TX PACKET BLOCK:Sending packet %f ") %i << std::endl;
		// Make SOB-tag and time-tag for data packet
		this->make_sob_tag(this->nitems_written(0)+i);
              	//this->make_time_tag(this->nitems_written(0)+i); 
	    } // End if(cpt_samples_packet == 1)

	    else if (cpt_samples_packet == d_nbr_samples_packet){	
		// Make EOB-tag for data packet
		this->make_eob_tag(this->nitems_written(0) + i); 
		fpga_time_available = false;
		cpt_samples_packet = 0;
	
		// Make SOB-tag and time-tag for next burst of zeros
		fracs_tx 	= fracs_tx + (d_nbr_samples_packet+1)/d_sample_rate; 
	    	double intpart; //normalize
            	fracs_tx 	= std::modf(fracs_tx, &intpart);
            	secs_tx  	= secs_tx + uint64_t(intpart);
		this->make_sob_tag(this->nitems_written(0)+i+1);
              	//this->make_time_tag(this->nitems_written(0)+i+1); 

			} // End else if(cpt_samples_packet == nbr_samples_packet)
		} // End if(fpga_time_available) 

	} // End for(int i = 0; i < (noutput_items); i++)
      
      

      // Tell runtime system how many output items we produced.
      return noutput_items;
    }
    
    
